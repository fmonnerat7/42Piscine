#include <unistd.h>

int	main(int argc, char **str)
{
	int	i;

	i = 0;
	argc --;
	while (str[argc][i] && argc > 1)
	{
		write(1, &str[argc][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return(0);
}
