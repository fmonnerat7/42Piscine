#include <unistd.h>

int	main(int argc, char **str)
{
	int	i;
	int	x;

	i = 0;
	x = 1;
	if (argc > 1)
	{
		while (x < argc)
		{
			i = 0;
			while (str[x][i] != '\0')
			{
				write(1, &str[x][i], 1);
				i++;
			}
			write(1, "\n", 1);
			x++;
		}
	}
	return (0);
}
