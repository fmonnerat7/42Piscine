#include <unistd.h>

int	main(int argc, char **str)
{
	int	i;
	int	x;

	i = 0;
	x = argc - 1;
	while (x > 0)
	{
		while (str[x][i] != '\0')
		{
			write(1, &str[x][i], 1);
			i++;
		}
		i = 0;
		x--;
		write(1, "\n", 1);
	}
	return (0);
}
